package com.company;

public class Leg {
    private int foot_size = 0;
    private boolean tats = false;
    private int leg_length = 0;

    public int getFoot_size() {
        return foot_size;
    }

    public void setFoot_size(int foot_size) {
        this.foot_size = foot_size;
    }

    public boolean isTats() {
        return tats;
    }

    public void setTats(boolean tats) {
        this.tats = tats;
    }

    public int getLeg_length() {
        return leg_length;
    }

    public void setLeg_length(int leg_length) {
        this.leg_length = leg_length;
    }
}
